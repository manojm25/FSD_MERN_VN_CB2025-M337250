let fruits = ["Apple", "Banana", "Mango", "Orange", "Grapes"];

console.log("Initial array:", fruits);

// push() – add at end
fruits.push("Pineapple");

// pop() – remove from end
fruits.pop();

// shift() – remove from start
fruits.shift();

// unshift() – add at start
fruits.unshift("Strawberry");

// Final array
console.log("Final array:", fruits);
